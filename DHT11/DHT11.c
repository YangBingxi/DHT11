#include "DHT11.h"
#include "delay/delay.h"
//复位DHT11
void DHT11_Rst(void)
{
    DHT11_IO_OUT();     //SET OUTPUT
//    DHT11_DQ_OUT=0;     //DQ=0
    DHT11_DQ_OUT_DISENABLE();     //拉低DQ
    Delay_ms(30);       //拉低至少18ms
//    DHT11_DQ_OUT=1;     //DQ=1
    DHT11_DQ_OUT_ENABLE();  //DQ=1
    Delay_us(30);       //主机拉高20~40us
}
//等待DHT11的回应
//返回1:未检测到DHT11的存在
//返回0:存在
uint8_t DHT11_Check(void)
{
    uint8_t retry=0;
    DHT11_IO_IN();//SET INPUT
    while (GPIOPinRead(GPIO_PORTC_BASE,GPIO_PIN_4)&&(retry<100))//DHT11会拉低40~80us
    {
        retry++;
        Delay_us(1);
    };
    if(retry>=100)return 1;
    else retry=0;
    while ((!GPIOPinRead(GPIO_PORTC_BASE,GPIO_PIN_4))&&(retry<100))//DHT11拉低后会再次拉高40~80us
    {
        retry++;
        Delay_us(1);
    };
    if(retry>=100)return 1;
    return 0;
}
//从DHT11读取一个位
//返回值：1/0
uint8_t DHT11_Read_Bit(void)
{
    uint8_t retry=0;
    while(GPIOPinRead(GPIO_PORTC_BASE,GPIO_PIN_4)&&retry<100)//等待变为低电平
    {
        retry++;
        Delay_us(1);
    }
    retry=0;
    while(!GPIOPinRead(GPIO_PORTC_BASE,GPIO_PIN_4)&&retry<100)//等待变高电平
    {
        retry++;
        Delay_us(1);
    }
    Delay_us(40);//等待40us
    if(GPIOPinRead(GPIO_PORTC_BASE,GPIO_PIN_4))return 1;
    else return 0;
}
//从DHT11读取一个字节
//返回值：读到的数据
uint8_t DHT11_Read_Byte(void)
{
    uint8_t i,dat;
    dat=0;
    for (i=0;i<8;i++)
    {
        dat<<=1;
        dat|=DHT11_Read_Bit();
    }
    return dat;
}
//从DHT11读取一次数据
//temp:温度值(范围:0~50°)
//humi:湿度值(范围:20%~90%)
//返回值：0,正常;1,读取失败
uint8_t DHT11_Read_Data(uint8_t *temp,uint8_t *humi)
{
    uint8_t buf[5];
    uint8_t i;
    DHT11_Rst();
    if(DHT11_Check()==0||1)
    {
        for(i=0;i<5;i++)//读取40位数据
        {
            buf[i]=DHT11_Read_Byte();
        }
        if((buf[0]+buf[1]+buf[2]+buf[3])==buf[4])
        {
            *humi=buf[0];
            *temp=buf[2];
        }
    }else return 1;
    return 0;
}
